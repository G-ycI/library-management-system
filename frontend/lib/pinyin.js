function getInitial(char) {
  if (!char || char.length !== 1) return '';
  for (const [initial, chars] of Object.entries(PY_MAP)) {
    if (chars.includes(char)) {
      return initial;
    }
  }
  return '';
}

export function getPinyinInitials(text) {
  if (!text) return '';
  let result = '';
  for (const char of text) {
    const initial = getInitial(char);
    result += initial || char.toLowerCase();
  }
  return result;
}

export function matchSearch(book, keyword) {
  keyword = keyword.toLowerCase();
  const title = (book.title || '').toLowerCase();
  const author = (book.author || '').toLowerCase();
  const titleInitials = getPinyinInitials(book.title || '').toLowerCase();
  const authorInitials = getPinyinInitials(book.author || '').toLowerCase();

  return (
    title.includes(keyword) ||
    author.includes(keyword) ||
    titleInitials.includes(keyword) ||
    authorInitials.includes(keyword)
  );
}
